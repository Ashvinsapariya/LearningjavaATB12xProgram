package ex_10_For_Loop;

public class Lab103_For_If_LOOP {
    public static void main(String[] args) {

        for( int RD=0; RD<18;RD++)
        {
            if (RD>15)
            {
                System.out.println("Gift from Papa, IPhone");
            }else
            {
                System.out.println("No Gift!");
            }
        }
    }
}
