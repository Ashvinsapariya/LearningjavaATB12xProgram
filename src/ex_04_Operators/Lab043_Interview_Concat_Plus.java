package ex_04_Operators;

public class Lab043_Interview_Concat_Plus {

    public static void main(String[] args) {
        String first_name = "RD";
        String last_name = "Desai";

        int a = 10;
        int b = 10;

        System.out.println(first_name + last_name + a + b); // PramodDutta1010
        System.out.println(a + b + first_name + last_name);

        System.out.println(first_name + last_name + (a + b));

        // BODMAS - Bracket of Div, mul, add, sub
//        BODMAS is an acronym that helps people remember the order of mathematical operations
//
//                - B: Brackets
//                - O: Order (powers, indices, or roots)
//                - D: Division
//                - M: Multiplication
//                - A: Addition
//                - S: Subtraction

    }
}


